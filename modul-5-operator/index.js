/** Aritmatika */

let number1 = 10;
let number2 = 5;

// penjumlahan
let resultPenjumlahan = number1 + number2;

console.log("Hasil penjumlahan: ", resultPenjumlahan);

// pengurangan
let resultPengurangan = number1 - number2;

console.log("Hasil pengurangan: ", resultPengurangan);

// perkalian
let resultPerkalian = number1 * number2;

console.log("Hasil perkalian: ", resultPerkalian);

// pembagian
let resultPembagian = number1 / number2;

console.log("Hasil pembagian: ", resultPembagian);

// sisa bagi
let resultSisaBagi = number1 % number2;

console.log("Hasil sisa bagi: ", resultSisaBagi);

/** Eksponen */

